This course does not tell students what is what?<br>
But introduces them to the whole concept development process of automobiles. 
The students are required to have some knowledge of the systems in an automobile, although we will introduce them to the students 
time to time as they get mentioned in the course. We also have various practical based technical activities to give a hands on experience 
and understanding. The students attending this workshop will go through the key steps of vehicle development, and actually formulating one 
vehicle design at the end of the workshop. As we said, this is not about “WHAT IS WHAT”, this is about “HOW AND WHY”! A whole new practical 
approach in the course content and interactive sessions make it a unique and a “MUST DO” workshop for enthusiastic students.<br><br>
 
<b>Major Sessions in this course are:-</b><br>
1. Ideation and Packaging<br>
2. Vehicle Systems Integration<br>
3. Design Exercise<br><br>
 
<b>Ideation and Packaging</b>
The development process of any automobile is based on both Designing and Engineering. A Designer must know certain aspects of the engineering 
and same applies to the Engineer as well. This session deals about how to conceptualize the idea of the vehicle that has a good design (Emotions)
 but is also as practical or functional for daily usage (Logic). The first look of the vehicle, body structure and dimensioning major parts will 
 be discussed in this session.<br>
 
1. Conceptualization<br>
2. Classification of the vehicles<br>
3. Body Types and Structures<br>
4. Major Dimensions<br>
5. Ergonomics and Cargo Space Considerations<br><br>
 
<b>Vehicle Systems Integration</b><br>
After deciding the initial structure and dimensions, the second session deals with the selection criteria of various automotive systems. 
Powertrain, Drivetrain, Suspension and Steering, Wheels and Tires, their types and Structures. The Systems will be introduces to the students and
 thoroughly discussed. Finalizing the complete model with all the technical and safety specifications of the vehicle.<br>
 
1. Powertrain Types and Selection Criteria<br>
2. Drivetrains Systems and Layouts<br>
3. Wheels and Tires<br>
4. Suspension and Steering System<br>
5. Braking System<br>
6. Advanced Safety Systems<br><br>
 
<b>Design Exercise</b><br>
The design Exercise lets the student make their own space-frame chassis with given (Finite) materials (Wires, soldering iron and soldering wire)
 for given dimensions. The students are divided into teams (3/4 students per team) and the chassis made by them will undergo various tests such as:<br>
 
1. Dimensions<br>
2. Payload Capacity<br>
3. Torsional Rigidity<br><br>

These tests will be done before the students on the testing rig that we’ve custom made. The chassis which passes all the tests above and weighs the 
minimum amongst other ones, WINS. A hands on experience that involves good creativity, engineering, craftsmanship and is also FUN-TO-DO.
