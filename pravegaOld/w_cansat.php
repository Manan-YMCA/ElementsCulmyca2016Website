<b>FAQ<b><br><br>

What are the prerequisites for the workshop?<br>

During workshop, we will start from the basics till the implementation of CAN Satellite. In addition, first one or two sessions may involve 
theoretical discussion (about kit development, sensors, hardware install, software and so on.). So, one can easily grab everything.<br><br>

What is the objective of the workshop?<br>

The objective of CAN Satellite is to conceptualize, design, build, test a small mock satellite. We use parachutes in order to launch the CAN Satellite. 
After launching, we will note down the various data sensed by the satellite. <br>

It is not possible to set up satellite in orbit since that requires permission from governmental bodies. Also, the budget of a real satellite is in the 
range of several crore rupees.
