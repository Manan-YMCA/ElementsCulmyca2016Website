				Be a part of the most creative automobile workshop. <b>Build Protoype of your dream CAR</b>, test your engineering and creativity.<br>
<br>
All On Auto brings to you "<b>The Automobile Blueprint</b>" - a workshop with an exciting Design exercise and Interactive sessions on Automobile Technology and design. <br>
Know how to build a car from ideation to execution. It provides you with an opportunity to build your own prototype scale model with Copper wires. It is just not the test of your engineering knowledge but Creativity, Team-Work, Time Management, resource Management and is also <b>FUN-TO-DO</b>.<br><br>

<b>Major Sessions in this course are</b>:-<br>
Ideation And Packaging<br>
Vehicle Systems Integration<br>
Design Exercise<br><br>

<b>Design Exercise</b><br>
The design Exercise lets the student make their own space-frame chassis with given (Finite) materials (Copper wire) for given dimensions. The students are divided into teams (4/5 students per team) and Get your Chassis tested against the odd forces. Let ' s see who can withstand all odds and WIN
<br><br>
1. Dimensions<br>
2. Payload Capacity<br>
3. Torsional Rigidity<br>
4. Overall weight<br><br>

These tests will be done before the students on the testing rig that we’ve custom made. The chassis which passes all the tests above and weighs the minimum amongst other ones, WINS. A hands on experience that involves good creativity, engineering, craftsmanship and is also FUN-TO-DO.
<br><br>

<b>Ideation And Packaging</b><br>
The development process of any automobile is based on both Designing and Engineering. A Designer must know certain aspects of the engineering and same applies to the Engineer as well. This session deals about how to conceptualize the idea of the vehicle that has a good design (Emotions) but is also as practical or functional for daily usage (Logic). The first look of the vehicle, body structure and dimensioning major parts will be discussed in this session.
<br><br>

1. Conceptualization<br>
2. Classification of the vehicles<br>
3. Body Types and Structures<br>
4. Major Dimensions<br>
5. Ergonomics and Cargo Space Considerations<br><br>


<b>Vehicle Systems Integration</b><br>
After deciding the initial structure and dimensions, the second session deals with the selection criteria of various automotive systems. Powertrain, Drivetrain, Suspension and Steering, Wheels and Tires, their types and Structures. The Systems will be introduces to the students and thoroughly discussed. Finalizing the complete model with all the technical and safety specifications of the vehicle.
<br>
Powertrain Types and Selection Criteria<br>
Drivetrains Systems and Layouts<br>
Wheels and Tires<br>
Suspension and Steering System<br>
Braking System<br>
Advanced Safety Systems<br>
				
