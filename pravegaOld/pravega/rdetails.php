<b>Part 2:	Registering for Competitions</b><br><br>

To participate in any competition, you must first complete the general registration procedure and get yourself a username provided by us.<br><br>

<b>For participating in team competitions:</b><br>
1. Each member of the team must have a valid Pravega username and must pay the registration fee as an individual.<br>
2. Go to the webpage of the competition your team wants to participate in and click on the “Register” button.<br>
3. In the dialog box that appears, enter the usernames of all your team members. Note that for all team competitions, 
while there is an upper limit on team size, there is no lower limit. So, if a competition mentions a team size of 5, 
even teams of 2 can participate, in which case the organizers will either let you continue as a team of 2 or pair you 
up with another such team.<br>
4. Your team is now all set to participate this competition.<br><br>

<b>For participating as an individual:</b><br>
1. You must have a valid Pravega username and must have paid the registration fee.<br>
