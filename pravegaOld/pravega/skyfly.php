<b>Session 1: Competition </b><br>
Time: 10.30 a.m<br>
<b>Round 1 (Quiz):</b><br>
The round 1 of the Skyfly competition will be a written technical quiz where each students will be given a questionnaire to solve. 
Questions will cover the basics of unmanned vehicles technology and will be based on the theoretical data.<br><br>
<b>Round 2 (UAV Modification):</b><br>
Each student/team will be given a standard resource pack according to the workshop they attended and using the kit, the teams have to 
modify the unmanned vehicle fabricated by them during the zonal workshop within stipulated time. The time duration will be announced on 
the day of the competition. The modifications made by the students in the existing unmanned vehicle should have a significant design impact 
and should also be able to increase the performance of the vehicle if tested in real world environment.
Each team member should give a short talk about the design change and points will be given based on individual performance.
For both the rounds, average points will be awarded and based on that the winners, first runner up, second runner up teams will be announced.<br><br>
